var api = "https://shopee.vn/api/v4/pages/get_category_tree";
var newCategoriesId = [];
var categoriesName = [];
var oldCategoriesId = [];
var categoriesLevel = [];
var categoriesImage = [];
var parentCategoriesId = [];
var tempIndex = 1;

await fetch(api)
    .then(function (response) {
        return response.json();
    })
    .then(function (response) {
        var categories = response.data.category_list;
        while (categories.length > 0) {
            var category = categories.shift();
            newCategoriesId.push(tempIndex);
            categoriesName.push(category.name);
            oldCategoriesId.push(category.catid);
            categoriesLevel.push(category.level);
            categoriesImage.push(category.image); // "https://down-vn.img.susercontent.com/file/" + category.image
            parentCategoriesId.push(category.parent_catid);
            tempIndex++;
            if (category.children != null) {
                categories = categories.concat(category.children);
            }
        }
    });

